								<h3 class="proj-title">
									<strong>Project:</strong> Food2
								</h3><span class="proj-time"><strong>Date:</strong> February, 2009</span> <span class="proj-role"><strong>Role:</strong> Developer</span>
							</div>
							<div class="proj-desc">
								<p>
									Food2 is to FoodNetwork like MTV2 is to MTV: Younger, hipper and full of rich media like videos and socially sharable content.  From the beginning we knew that this site was going to be a challenge because it had two separate launches. One on WordPress as a blog "on steroids", and then a second one as a full-blown site on our in-house content management system.
								</p>
								<p>
									Because the site soft-launched on a blogging platform, we decided to "out source" the social aspect of the site to Facebook, via its Facebook Connect service.  Through in-house plugins and extensive JS manipulation, we were able to create a fully interactive site built on the back of Facebook's social graph back bone. 
								</p>
								<p>Below is a screenshot of a member page.  Keep in mind that 100% of the "member" activity is being powered by Facebook, without an load or complication on our part.  Every activity on our site was cross-promoted on Facebook to attract a significant amount of new members.  This site hasn't launched yet, so <strong>ssshh....</strong></p>
