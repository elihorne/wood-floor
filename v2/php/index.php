								<h3 class="proj-title">
									<strong>Project:</strong> HGTV
								</h3><span class="proj-time"><strong>Date:</strong> December, 2008</span> <span class="proj-role"><strong>Role:</strong> Developer</span>
							</div>
							<div class="proj-desc">
								<p>
									By the time HGTV came down the pipe, our team had the benefit of a team of developers who had already worked in close proximity (foodnetwork.com).  As a result, we were able to take this project from PSDs to a living, breathing website in just over 2 months.  Although we were using the <abbr title="Design Pattern Library">DPL</abbr> for the second time, much of it was optimized and improved to support new UX behaviors.</p>
								<p>
									The biggest challenge with this site (and FoodNetwork.com) was the extensive use of alpha transparency, and animated modules that needed to be modular-enough to jump columns and change widths.  Because several of the right and left rail modules animated upwards, IE6 was a major liability for the development team.  With some careful progressive enhancement, we packaged up a site that functions at 100% for all browsers, with a little extra gloss and sparkle for visitors with standards-compatible browsers. 
								</p>
								
								<p>Below is a screenshot of the homepage with one of the main level dropdowns activated.  This experience was achieved across all our major browsers.  Visit the full site: <a href="http://www.hgtv.com">HGTV.com</a></p>
